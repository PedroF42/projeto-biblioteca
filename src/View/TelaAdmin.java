    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        //Pegar linha selecionada

        try {
            EntityManager em = Persistence.createEntityManagerFactory("BibliotecaTestePU").createEntityManager();
            ReservaJpaController rc = new ReservaJpaController(Persistence.createEntityManagerFactory("BibliotecaTestePU"));
            DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();

            if (tabela.getSelectedRow() == -1) {
                throw new Exception("Selecione uma reserva");
            }

            int resposta = JOptionPane.showConfirmDialog(null, "Você realmente deseja cancelar a reserva do dia " + tabela.getValueAt(tabela.getSelectedRow(), 2) + " às " + tabela.getValueAt(tabela.getSelectedRow(), 3) + " da sala "
                    + tabela.getValueAt(tabela.getSelectedRow(), 1) + "?", "Cancelar Reserva", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
                Reserva res = (Reserva) em.createNamedQuery("Reserva.findByCodReserva").setParameter("codReserva", tabela.getValueAt(tabela.getSelectedRow(), 0)).getSingleResult();
                rc.destroy(res.getReservaPK());
                modelo.removeRow(tabela.getSelectedRow());
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnCancelarActionPerformed

